// Regenerates netlify/functions/og-assets.mjs — the fonts + tile art the
// og-image function embeds as base64 (no fs paths to break between local
// dev and the Lambda bundle). Run from the repo root:
//   node scripts/gen-og-assets.mjs
import { readFileSync, writeFileSync } from 'node:fs';

const b64 = (p) => readFileSync(p).toString('base64');

const out = `// GENERATED by scripts/gen-og-assets.mjs — do not edit by hand.
// Base64-embedded render assets for og-image (fonts + tile art), so the
// function bundle is self-contained on every runtime.
export const INTER_BOLD_B64 = '${b64('assets/fonts/Inter-Bold.ttf')}';
export const INTER_SEMIBOLD_B64 = '${b64('assets/fonts/Inter-SemiBold.ttf')}';
export const TILE_PNG_B64 = '${b64('assets/tile.png')}';
`;
writeFileSync('netlify/functions/og-assets.mjs', out);
console.log('wrote netlify/functions/og-assets.mjs');
